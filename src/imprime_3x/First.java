                + "----------------------------\n;"
                + "    Numero de lote     " + lote + "       \t;"
                + "############################\n;"
                + "         " + list + "         \t;"
                + "############################\n;"
                + "OF                   " + of + "       \t;"
                + "Modelo               " + model + "     \t;"
                + "QNT.                 " + qnt + "    \t;"
                + "TAM.                 " + tam + "  \t;"
                + "NUMERAÇÃO            " + numeracao + "  \t;"
                + validaTotal(total)
                + "----------------------------\t;"
                + "-----------DIELMAR----------\t;"
                + "------------" + year + "------------\t;"
                + "----------------------------\t;";

        Printnow p = new Printnow();

